// Assignment 1 - The Truth Table (Comparison Operators)

// Declare variables
let requiredAge = 18;
let userAge = "18"; // string

// Loose equality (compares value only)
console.log(userAge == requiredAge);

// Strict equality (compares value AND type)
console.log(userAge === requiredAge);

// Not strictly equal
console.log(userAge !== requiredAge);

// Bonus
console.log(5 > 2);
console.log(10 <= 10);